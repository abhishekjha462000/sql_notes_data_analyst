-- SQL set operations

1. UNION: The UNION operator is used to combine the results of two or more select statements.
It removes the duplicate rows between the various SELECT statements.

2. UNION ALL: The UNION ALL operator is similar to the UNION operator, but it does not remove duplicate rows from the result set.

3. INTERSECT: The INTERSECT operator returns only the rows that appear in both result sets of two SELECT statements.

4. EXCEPT: The EXCEPT operator returns only the distinct rows that appear in the first result set
and removes the common rows.

